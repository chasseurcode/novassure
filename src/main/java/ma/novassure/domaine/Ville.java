package ma.novassure.domaine;


/**
 * @author TARAM & BODIE
 */
public class Ville {

    public Ville() {
    }

    private int id;
    private String code;
    private String nom;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}

    
   
}